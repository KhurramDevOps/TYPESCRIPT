//  CONDITIONAL STATEMENT

// What is Conditional statement ?
// Conditional statement is a statement that is used to execute a block of code if a certain condition is
// met. It is used to make decisions in a program.
// There are two types of conditional statements in TYPESCRIPT:
// 1. if statement
// 2. else if  statement
// 3. else statement

let Name = "Khurram";
let Age = 25;

if (Name == "Khurram" && Age == 25) {
  console.log("Hello Khurram, you are 25 years old.");
} else if (Name == "ALI" && Age == 19) {
  console.log("Hello ALi, you are 19 years old.");
} else {
  console.log("Hello Stranger");
}
